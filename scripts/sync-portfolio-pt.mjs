const idMigrations = {
  'programmatic-advertising-analytics-dashboard-2019': 'programmatic-advertising-analytics-bi-platform-2019',
}

function localizeProject(enProject) {
  const migratedFrom = idMigrations[enProject.id]
  const existing =
    ptById[enProject.id] ?? (migratedFrom ? ptById[migratedFrom] : null)
  const translated = newTranslations[enProject.id]

  const base = translated ?? existing
  if (!base) {
    throw new Error(`Missing PT translation for project: ${enProject.id}`)
  }

  return {
    ...base,
    id: enProject.id,
    year: enProject.year,
    company: enProject.company,
    disclosure: enProject.disclosure,
    teamSize: enProject.teamSize,
    proudestShowcase: enProject.proudestShowcase,
    proudestRanking: enProject.proudestRanking,
    name: translated?.name ?? (migratedFrom ? 'Dashboard de Analytics de Publicidade Programática' : base.name),
  }
}

